
public class Calculator {
	
	public double add(double a, double b){
		return a+b;
	}
	public double minus(double a, double b){
		return a-b;
	}	
	public double multiply(double a, double b){
		double c = a*b;
 		return c;
	}	
	public double division(double a, double b){

		double c = a/b;
		
		return  c ;
	}

}
